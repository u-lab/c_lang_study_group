/*参照するライブラリを指定*/
#include <stdio.h>

/*戻り値の型　関数名　(引数の型)*/
int main(void)
{
	/*配列の要素数を決めた*/
	/*代入した値によらず定義した要素数の範囲は値を入れることができる。*/
	char s[10] = "Hello!";
	int d[20] = {1,2,3};
	int i;

	/*一括代入はできない！*/
	// s = "ABCDE";
	// d = {4,5,6};

	/*値を書き換えるときは一つずつ*/
	d[0] = 4;
	d[1] = 5;
	d[2] = 6;
	/*文字配列でも連続ならこうやって書き換えることができる！*/
	for (i = 0; i < 5; i++)
		s[i] = 'A' + i;
	/*null文字の代入*/
	s[i] = '\0';

	/*値の確認*/
	printf("%s\n" , s);
	/*main関数の戻り値*/
	return 0;
}

