/*参照するライブラリを指定*/
#include <stdio.h>

/*戻り値の型　関数名　(引数の型)*/
int main(void)
{
	/*宣言*/
	/*型　配列名　[配列の大きさ]*/
	int	a[5];
	char b[20];

	/*要素の入れ方*/
	/*配列名[添え字] = 入れたい値。*/
	a[0] = 10;
	b[3] = 'c';

	/*初期値であれば中カッコでいっぺんに入れることもできる。*/
	int	c[3] = {5, 1, 3};
	/*この時は、c[0] = 5, c[1] = 1, c[2] = 3*/
	/*いっぺんに初期化することも可能*/
	int d[4] = {0};

	/*繰り返し構文用の添え字*/
	int	i;
	int	ans;
	/*この書き方であればiは10未満、つまり9まで動く*/
	for (i = 0; i < 5; i++)
			a[i] = i;

	/*main関数の戻り値*/
	return 0;
}

