/*参照するライブラリを指定*/
#include <stdio.h>

/*戻り値の型　関数名　(引数の型)*/
int main(void)
{
	/*for文のための変数*/
	double i;

	/*for文の書き方*/
	/*for (初期値; 制限; 増減率)*/
	/*今回は*/
	/*for (1.0からスタート; 2.0まで; 0.1ずつ増える！)*/
	/*i<2.1でも大丈夫！*/
	for (i = 1.0; i <= 2.0; i += 0.1)
	{
		printf("%f\n",i);
	}

	/*main関数の戻り値*/
	return 0;
}

